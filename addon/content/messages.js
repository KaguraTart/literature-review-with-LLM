function zmsResolveUiLanguage(setting, locale) {
  if (setting === "zh-CN" || setting === "en-US") return setting;
  return String(locale || "").toLowerCase().startsWith("zh") ? "zh-CN" : "en-US";
}

function zmsMessage(scope, key, settingOrLocale, runtimeLocaleValue) {
  let setting = settingOrLocale;
  let locale = runtimeLocaleValue;
  if (runtimeLocaleValue === undefined && !zmsIsLanguageSetting(settingOrLocale)) {
    setting = null;
    locale = settingOrLocale;
  }
  const lang = zmsResolveUiLanguage(setting, locale);
  const fromScope = ZMS_I18N[lang]?.[scope] || {};
  if (fromScope?.[key]) return fromScope[key];
  const fallback = ZMS_I18N["en-US"]?.[scope] || {};
  return fallback?.[key] || key;
}

function zmsIsLanguageSetting(value) {
  return value === "zh-CN" || value === "en-US";
}
